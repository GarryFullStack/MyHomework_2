def min_boats(m, n, weights):
    """
    Функция вычисляет минимальное количество лодок для перевозки всех рыбаков.

    :param m: Максимальный вес, который выдерживает лодка
    :param n: Количество рыбаков
    :param weights: Список весов рыбаков
    :return: Минимальное количество лодок
    """
    # -*- coding: cp1251 -*-
    weights.sort()  # Сортируем веса рыбаков по возрастанию
    left, right = 0, n - 1  # Два указателя: левый (на самого легкого), правый (на самого тяжелого)
    boats = 0  # Счетчик лодок

    while left <= right:  # Пока есть непереправленные рыбаки
        if left == right:  # Если остался один рыбак
            boats += 1  # Он занимает отдельную лодку
            break  # Завершаем цикл

        if weights[left] + weights[right] <= m:  
            # Если самый легкий и самый тяжелый помещаются вместе в лодку
            left += 1  # Перемещаем левый указатель (этот рыбак сел в лодку)
        
        right -= 1  # Перемещаем правый указатель (этот рыбак всегда садится в лодку)
        boats += 1  # Добавляем лодку

    return boats  # Возвращаем итоговое количество лодок

# Ввод данных
m = int(input("Введите максимально допустимый вес на одну лодку: ").strip())  # Читаем максимальный вес лодки
n = int(input("Введите количество рыбаков: ").strip())  # Читаем количество рыбаков
weights = [int(input("Введите вес каждого из указанного количества рыбаков: ").strip()) for _ in range(n)]  # Читаем веса рыбаков в список

# Вывод результата
print(min_boats(m, n, weights))  # Вызываем функцию и печатаем ответ
